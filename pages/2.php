<h3>The First Irrational Number</h3>
<p>	Another revolution occurred when Heroditus discovered that not all numbers are natural, whole, integers, fractions, or rational. He discovered a new kind of number: the irrationals. The discovery cost Heroditus his life. The Pythagoreans' beliefs were based on the idea that all numbers were integers, and this new idea was so shocking to them that they killed Heroditus.</p>
<p>The proof went something like this. You have a square with sides with lengths of 1. The hypotaneuse moves diagonally through the center of the square, and we have two triangles. Apply the Pythagorean Theorum
<math class="noblock" xmlns='http://www.w3.org/1998/Math/MathML'>
	<msup>
		<mi>a</mi>
		<mn>2</mn>
	</msup>
	<mo>+</mo>
	<msup>
		<mi>b</mi>
		<mn>2</mn>
	</msup>
	<mo>=</mo>
	<msup>
		<mi>c</mi>
		<mn>2</mn>
	</msup>
</math>
. When we plug in the numbers we have 
<math class="noblock">
	<mn>1</mn>
	<mo>+</mo>
	<mn>1</mn>
	<mo>=</mo>
	<msup>
		<mi>c</mi>
		<mn>2</mn>
	</msup>
</math>
. Simplified we have 
<math class="noblock">
	<mn>2</mn>
	<mo>=</mo>
	<msup>
		<mi>c</mi>
		<mn>2</mn>
	</msup>
</math>

. If we take the square root of both sides, we see that C is equal to the square root of 2.</p>
<p>The Pythagoreans thought the length of any hypotanuese was an integer. However, we can prove that the square root of 2 is not an integer.<p>
<p>A rational number can be represented by the fraction <math><mfrac><mi>p</mi><mi>q</mi></mfrac></math>, where p stands for the product and q for the quotient. Thus we make the equation <math><mfrac><mi>p</mi><mi>q</mi></mfrac><mo>=</mo><msqrt><mn>2</mn></msqrt></math>. We can remove the square root by squaring both sides and simplify to <math><mn>2</mn><msup><mi>q</mi><mn>2</mn></msup><mo>=</mo><msup><mi>p</mi><mn>2</mn></msup></math>. This is the final statement, and one that proves the impossibility of square root of 2 ever being an integer.</p>
<p>The prime factorization of any number squared will either have an even number of 2's in it or no 2's at all. In this case, there is another 2 multiplied to the 2, and therefore we will always have an odd number of 2's on the left side of the equation. The left side will never equal an even number of 2's (or no 2's at all) of the prime factorization on the right side of <math><msup><mi>p</mi><mn>2</mn></msup></math>. We are forced to conclude that square root of 2 cannot be a rational number, and therefore must be irrational.</p>
<p>Another number system is born!</p>

