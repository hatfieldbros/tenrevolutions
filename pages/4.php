<h3>Ulam Spiral</h3>
<p>A prime is any number whose only factors are one and itself. These unique numbers do not follow the pattern of any other number we know about.</p>
<p>The search for some sort of pattern in prime numbers has been a quest since these unique numbers were discovered. Around 300 BC, Euclid challenged the belief that there could be a finite amount of prime numbers. His proof is one of the oldest theorems in math.</p>
<p>Throughout history, people have tried to find some sort of pattern among prime numbers. Can you find one? Fortunately for us, there is not a clear pattern for all the prime numbers. Why? Because the safety of our electronic transactions relies on the uniqueness of prime numbers. (See page IX for a more detailed explanation on that)</p>
<p>Stan Ulam (1909-1984) tried a new approach to looking at primes. He developed the spiral of numbers you see on the right. First he layed out all the whole numbers in a spiral. Second, he highlighted the prime numbers. Its that simple. There still doesn't seem to be any patterns using this method, although it is nice to look at.</p>

