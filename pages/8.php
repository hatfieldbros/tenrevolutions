<h3>The Euler Characteristic</h3>
<div class="boolean_svg">
<object class="topology" data="../svgs/8/A1.svg" type="image/svg+xml" ></object>
</div>
<p>Like graph theory (see page 1), this section is under the study of topology. However, instead of just studying edges and vertices, we add faces. The well-known mathematician, Euler, discovered certain characteristics that all two-dimensional objects possess. He formulated that if you add all the vertices of an object, subtract the edges, and finally add the number of faces, the sum will always result in the same number.</p>
<h3>The Alphabet</h3>
<p>Check out the A to the right. We will examine this first letter of the alphabet. Euler examined letters by looking at the relationships between the edges, vertices, and faces. He characterized an object by assigning exactly one number to it. And to get this number, he subtracted the vertices and faces from the edges. He discovered that any object with exactly one hole in it would have a Euler Characteristic of 0.</p>
<p>Count the edges, vertices, and faces of the A to the upper right. Check your answer by clicking on the boxes and see the animation. Euler reduced his discovery to the formula 1 - g. G represents the number of holes in a letter. Try the letter B. Draw it with edges, vertices, and faces and see if you come up with the number -2.</p>
