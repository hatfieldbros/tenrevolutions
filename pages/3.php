<h3>Triangle Miracles</h3>
<p>There are many interesting properties of triangles that you may have never seen or guessed. Just to name a few: there is a central point for every triangle found by drawing bisecting lines from the three vertices; there is an inside circle around that central point that is tangent to each of the three sides; and there is an equilateral triangle inside every triangle. There are many more secrets about triangles but lets take a closer look at these hidden equilateral triangles.</p>
<h3>Morley's Miracle</h3>
<p>Just a little over 100 years ago, Frank Morley was a college professor near Philadelphia, Pennsylvania. In 1899 he discovered the fact that there is a special triangle inside every triangle. Maybe we should first talk about triangles. You must know that some have three acute angles, and some have one obtuse or right angle. Then the extra special triangles are called iscosolese and equilateral. These have special characteristics like similar sides and angles. To be exact, in an equilateral triangle, all three angles are the same measurement, and all sides are the same length. Do you believe that there is one of these inside any triangle you can draw?</p>
<p>First, lets start with any triangle. I have drawn a triangle to the right. Next, we trisect, or split into three equal parts, each corner angle. Click the vertices to see the result. Morley discovered that if you take the intersections of these trisecting lines and make new vertices, these will become the corners of an equilateral triangle. So that small triangle in the middle has sides of equal length and all the corner angles are the same.</p>
<p>Give it a try yourself. Draw any shaped triangle (remember, it must have three sides to be a triangle), and then trisect each interior angle. Draw a small dot on the intersections that the outermost trisecting lines form when they cross each other. Connect the dots and you've seen another revolution.</p>
